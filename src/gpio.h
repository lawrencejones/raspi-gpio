/////////////////////////////////////////////////////////////
// GPIO Read ////////////////////////////////////////////////
Pin                 *get_pin_status(int p);
Pin                 *update_pin_status(Pin* pin);
Pin                 **update_all_pins();

/////////////////////////////////////////////////////////////
// GPIO Write ///////////////////////////////////////////////
void                set_with_word(uint32_t w);
void                clr_with_word(uint32_t w);
void                set_pin_value(int p, int v);
void                set_pin_state(int p, int v);

/////////////////////////////////////////////////////////////
// GPIO Display /////////////////////////////////////////////
void                printHeader();
void                printPin(Pin *pin);
void                printAll();

