// GPIO INTERFACE
///////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////
// GPIO Initialization //////////////////////////////////////
volatile unsigned*  init_gpio_access();

/////////////////////////////////////////////////////////////
// GPIO Malloc / Dealloc ////////////////////////////////////
Chip                *init_chip();
Pin                 *malloc_pin(int p);
void                dealloc_chip();
