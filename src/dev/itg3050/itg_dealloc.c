#include <stdlib.h>
#include <string.h>
#include "itg_private.h"

///////////////////////////////////////////////////////////////////////////////
// DEALLOC ITG
///////////////////////////////////////////////////////////////////////////////

void itg_dealloc(Sensor **s)
{
  // If sensor is already null
  if ((*s) == NULL)
  {
    // The return without doing anything
    return;
  }
  // Free the sensor struct
  free(*s);
  // Null the pointer
  *s = NULL;
}
